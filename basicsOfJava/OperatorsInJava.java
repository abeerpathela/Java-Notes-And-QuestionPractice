public class OperatorsInJava {
    public static void main(String[] args){

        // Arithmetic

        double a=3;
        double b=2;
        double c=a-b;
        double d=a+b;
        double e=a*b;
        double f=a/b;
        double g=a%b;
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);
        System.out.println(f);
        System.out.println(g);

        // Assignment

        System.out.println(++a);
        System.out.println(a++);
        System.out.println(a);
        

    }
}
